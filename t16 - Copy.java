public class t16 {
   
        public static void main(String[] args) {
            int n = 5; 
    
            
            for (int i = 0; i < n; i++) {
                
                for (int j = 0; j < n; j++) {
                    System.out.print(Math.abs(i - j) + " ");
                }
    
                System.out.println();
            }
        }
    }
    

